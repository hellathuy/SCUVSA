						<h3 class="lfd">
							<p>This year’s show is about <b>what it means to be Vietnamese-American: how we see ourselves and how we perceive and interact with the people around us.</b> The show aims to address contemporary Vietnamese-American issues such as prejudice against other minorities, especially against black people, as well as our struggle to conform to (or rebel against) the model minority label despite our very different background as refugees and children of refugees. What distinguishes our show from any other medium is the ability and opportunity to act out these uncomfortable scenarios so that we can develop the skills and confidence to do so offstage. Therefore, we hope not only to promote awareness of these issues but also empower participants and attendees to take the first step in initiating these difficult dialogues and ultimately becoming agents of change within their own families and local communities. In light of recent political events, it is more important than ever to unify our communities to fight against injustice.</p>
