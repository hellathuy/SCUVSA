				<div class="row botspace-sm">
					<div class="col-md-12">
						<h4>
							<p>The Áo Bà Ba dance is a happy and carefree dance that focuses a lot on the relationships and love between villagers in the Vietnamese countryside. The attire for this dance, called áo bà ba, is supposed to be easy to move in and tend to be darker colors to hide the grime of their work since villagers tend to get dirty in the fields. However, in this dance, we are wearing lavender áo bà bas to bring out the romance between couples and nostalgia for the Vietnamese people who used to live or work in the countryside. The Áo Bà Ba dance carries a lot more than the love and romantic moves; it also includes the memory of the country side and the Vietnamese pride.</p>
							
							<p><i>Dancers: Thao Nguyen-Pham, Truc Nguyen-Pham, Jessica Duong, Lucia Ho, Cherry Ng, Thuy Le, Keola McDonald, Peter Pham, Khang Nguyen, Lyman Shen, Alan Nguyen, Vincent Pham</i></p>
