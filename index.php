							<p>Our nón lá dance is a traditional Vietnamese dance that incorporates nón lá (conical hats) into the choreography. Since this hat is a symbol of charm and grace, the dance will be very flowy and gentle, as to create a soothing effect. Additionally, our song is about the beauty of Vietnamese women and dancers will wear áo dài to showcase this.</p>
							
